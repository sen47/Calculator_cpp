#include "Operations.h"

template<typename T>
inline T add(const T F, const T S)
{
	return F + S;
}

template<typename T>
inline T subtract(const T F, const T S)
{
	return F - S;
}

template<typename T>
inline T multiple(const T F, const T S)
{
	return F * S;
}

template<typename T>
inline T divide(const T F, const T S)
{
	return F / S;
}
